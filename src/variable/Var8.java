package variable;

public class Var8 {
    public static void main(String[] args) {
        //정수
        byte b = 127; //-128~127
        short s = 32767;//-32,768~32,767
        int i = 2147483647;//-2,147,483,648~2,147,483,647 *
        long l = 9223372036854775807L;//뒤에 L 붙여줘야 함.*

        //실수
        float f = 10.0f;//뒤에 f 붙여줄 것.
        double d = 1.0;//좀 더 큰 범위   *
    }
}
